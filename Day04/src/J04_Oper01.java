
public class J04_Oper01 {
	public static void main(String[] args) {
		
//		산술연산자 : + - * / %
//		- 수치 데이터에 사용 가능
		
		int i1, i2;
		i1 = 22;		i2 = 5;

		System.out.println(i1 + i2);
		System.out.println(i1 - i2);
		System.out.println(i1 * i2); // 곱하기
		System.out.println(i1 / i2);
//		=> 나누고 난 후, 몫에만 관심있음
		System.out.println(i1 % i2);
//		=> mod(모드)연산자
//		=> 나누고 난 후, 나머지에만 관심 있음
//		=> 짝수-홀수 판별, 배수 판별 할 때 사용됨
		
		
	}
}















