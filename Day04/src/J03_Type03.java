
public class J03_Type03 {
	public static void main(String[] args) {
		
		String s;
//		=> 문자열(문장)을 저장할 수 있는
//			기능을 가진다.
		
		s = "안녕. 난 둘리야";
		System.out.println("s : " + s);
		
		s = "1234";
//		=> 문자열 형태로 표현된 숫자들은
//			실제 숫자가 아니고, 그림 형식의
//			정보일 뿐이므로, 계산 불가능하다.
		System.out.println("s : " + s);
		
		
		s = null;
//		=> String 공간은 빈공간을 의미하는
//			null이라는 메시지를 저장할 수 있다.
//		=> 보통 String 공간을 만드는 시점에서
//			문자열(문장)을 바로 저장하지 않는
//			경우, 기본값으로 null이라는 메시지를
//			저장함으로써 프로그램의 안정성을
//			높이는 효과가 있다.
		
//		사용 예
		String str = null;
		
	}
}
